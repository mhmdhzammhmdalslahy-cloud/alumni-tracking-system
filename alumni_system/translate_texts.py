def process_file(filepath):
    """معالجة ملف واحد واستبدال النصوص"""
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
        
        changed = False
        for old_text, new_text in replacements.items():
            # التأكد من عدم استبدال النصوص الموجودة بالفعل داخل {% trans %}
            if '{% trans "' + old_text + '" %}' in content:
                continue  # تمت ترجمته مسبقاً
            if old_text in content:
                content = content.replace(old_text, new_text)
                changed = True
                print(f"   🔄 استبدال: '{old_text}' → '{new_text[:30]}...'")
        
        if changed:
            with open(filepath, 'w', encoding='utf-8') as f:
                f.write(content)
            print(f"✅ تم تعديل: {filepath}")
            return True
        return False
    except Exception as e:
        print(f"⚠️ خطأ في {filepath}: {e}")
        return False

def main():
    """الوظيفة الرئيسية: المسح على جميع القوالب"""
    print("=" * 60)
    print("🌍 أداة تحويل النصوص إلى علامات ترجمة (i18n)")
    print("=" * 60)
    print()
    
    templates_dir = 'templates'
    if not os.path.exists(templates_dir):
        print(f"❌ مجلد {templates_dir} غير موجود!")
        return
    
    count = 0
    # المسح على جميع ملفات HTML في مجلد templates
    for root, dirs, files in os.walk(templates_dir):
        for file in files:
            if file.endswith('.html'):
                filepath = os.path.join(root, file)
                if process_file(filepath):
                    count += 1
    
    print()
    print("=" * 60)
    print(f"🎉 تم الانتهاء! تم تعديل {count} ملف.")
    print("=" * 60)
    print()
